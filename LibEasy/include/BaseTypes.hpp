#ifndef LEC_BASETYPES_H_
#define LEC_BASETYPES_H_

#include <cinttypes>

namespace Easy {

/**
 * \brief basic type alias.
 * 
 * Alias of signed 8 bits int.
 * */
typedef uint8_t e_ubyte;

/**
 * \brief basic type alias.
 * 
 * Alias of char.
 * */
typedef char	e_byte;

/**
 * \brief basic type alias.
 * 
 * Alias of unsigned 16 bits int.
 * */
typedef uint16_t e_ushort;

/**
 * \brief basic type alias.
 * 
 * Alias of signed 16 bits int.
 * */
typedef int16_t e_short;

/**
 * \brief basic type alias.
 * 
 * Alias of unsigned 32 bits int.
 * */
typedef uint32_t e_uint;

/**
 * \brief basic type alias.
 * 
 * Alias of signed 32 bits int.
 * */
typedef int32_t e_int;

/**
 * \brief basic type alias.
 * 
 * Alias of unsigned 64 bits int.
 * */
typedef uint64_t e_ulong;

/**
 * \brief basic type alias.
 * 
 * Alias of signed 64 bits int.
 * */
typedef int64_t e_long;

/**
 * \brief basic type alias.
 * 
 * Alias of bool.
 * */
typedef bool    e_bool;

/**
 * \brief basic type alias.
 * 
 * Alias of wchar_t.
 * */
typedef wchar_t e_char;

/**
 * \brief basic type alias.
 * 
 * Alias of float.
 * */
typedef float   e_float;

/**
 * \brief basic type alias.
 * 
 * Alias of double.
 * */
typedef double  e_double;

/**
 * \brief basic type alias.
 *
 * Alias of void.
 * */
typedef void	e_void;

} // namespace Easy

#endif // LEC_BASETYPES_H_